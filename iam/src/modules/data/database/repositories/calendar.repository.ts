 // Helper function to extract base asset from trading pair (e.g., "ETHUSDT" -> "ETH")
  private extractBaseAsset(symbol: string): string {
    return symbol.replace(/USDT$/, '').toUpperCase();
  }

  // Get events by date range
  async getEventsByDateRange(startDate: number, endDate: number): Promise<CalendarEvent[]> {
    return this.events.filter(
      (event) => event.fetched_at >= startDate && event.fetched_at <= endDate
    );
  }

  // Get events by asset symbol (handles trading pairs like ETHUSDT)
  async getEventsBySymbol(symbol: string): Promise<CalendarEvent[]> {
    const baseAsset = this.extractBaseAsset(symbol);
    const res = this.events.filter((event) =>
      event.related_assets.includes(baseAsset)
    );
    return res;
  }

  // Get events by event type
  async getEventsByType(eventType: string): Promise<CalendarEvent[]> {
    return this.events.filter(
      (event) => event.event_type.toLowerCase() === eventType.toLowerCase()
    );
  }

  // Get events by market segment
  async getEventsByMarketSegment(segment: string): Promise<CalendarEvent[]> {
    return this.events.filter(
      (event) => event.market_segment.toLowerCase() === segment.toLowerCase()
    );
  }

  // Get all events
  async getAllEvents(): Promise<CalendarEvent[]> {
    return this.events;
  }
